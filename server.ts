import express from 'express';
import { createServer as createViteServer } from 'vite';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

async function startServer() {
  const app = express();
  const PORT = 3000;

  app.use(express.json());

  // API Routes
  app.get('/api/health', (req, res) => {
    res.json({ status: 'ok', timestamp: new Date().toISOString() });
  });

  // Mock PDF Generation Endpoint
  app.post('/api/reports/generate/:inspectionId', async (req, res) => {
    const { inspectionId } = req.params;
    // In a real implementation, we would fetch data from Firestore
    // and use a library like jspdf or pdfkit to generate a PDF.
    res.json({ 
      success: true, 
      message: `Report generation started for ${inspectionId}`,
      url: `/reports/${inspectionId}_draft.pdf`
    });
  });

  // Vite middleware for development
  if (process.env.NODE_ENV !== 'production') {
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: 'spa',
    });
    app.use(vite.middlewares);
  } else {
    const distPath = path.join(process.cwd(), 'dist');
    app.use(express.static(distPath));
    app.get('*', (req, res) => {
      res.sendFile(path.join(distPath, 'index.html'));
    });
  }

  app.listen(PORT, '0.0.0.0', () => {
    console.log(`NEXUM Server running on http://localhost:${PORT}`);
  });
}

startServer();
